package com.dialogflow.dialogflowforbrowser.modelforgoogleactions;

public class PackageEntitlements {

	private String packageName;
	private Entitlement[] entitlements;
	
	public String getPackageName() {
		return packageName;
	}
	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}
	public Entitlement[] getEntitlements() {
		return entitlements;
	}
	public void setEntitlements(Entitlement[] entitlements) {
		this.entitlements = entitlements;
	}
}
